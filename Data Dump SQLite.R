library(sqldf)
db <- dbConnect(SQLite(), dbname="../db/restaurantPredictions.db", flags=SQLITE_RWC)
rest <- read.csv("../csv/restaurants.csv", as.is=TRUE)
lab <- read.csv("../csv/labels.csv", as.is=TRUE)
dbWriteTable(conn=db, name="Restaurants", value=rest, row.names=FALSE)
dbWriteTable(conn=db, name="Labels", value=lab, row.names=FALSE)
dbReadTable(db, "Restaurants")
dbReadTable(db, "Labels")
dbDisconnect(db)
